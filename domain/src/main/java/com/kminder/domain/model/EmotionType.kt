package com.kminder.domain.model

/**
 * 로버트 플루치크(Robert Plutchik)의 8가지 원형 감정
 */
enum class EmotionType {
    /** 분노 */
    ANGER,
    
    /** 기대 */
    ANTICIPATION,
    
    /** 기쁨 */
    JOY,
    
    /** 신뢰 */
    TRUST,
    
    /** 두려움 */
    FEAR,
    
    /** 슬픔 */
    SADNESS,
    
    /** 혐오 */
    DISGUST,
    
    /** 놀람 */
    SURPRISE
}
